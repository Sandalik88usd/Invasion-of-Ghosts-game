        public static void LookAtThePAperInScaner()
        {
            Console.Clear();
            Console.CursorVisible = false;
            Console.WindowWidth = 77;
            Console.BufferWidth = 77;
            Console.WindowHeight = 50;
            Console.BufferHeight = 50;
            FirstPosterInScaner();
            Animation.WriteAt("Press Escape to come back.", 20, 48);

            ConsoleKey key = Console.ReadKey().Key;
            int numberOfPaper = 0;
            while (key != ConsoleKey.Escape)
            {
                key = Console.ReadKey().Key;
                if (key == ConsoleKey.Enter)
                    numberOfPaper++;
                if (numberOfPaper == 3)
                    numberOfPaper = 1;
                switch (numberOfPaper)
                {
                    case 1:
                        FirstPosterInScaner();
                        Animation.WriteAt("Press Escape to come back.", 20, 48);
                        break;
                    case 2:
                        SecondPosterInScaner();
                        Animation.WriteAt("Press Escape to come back.", 20, 48);
                        break;
                }
            }
        }
